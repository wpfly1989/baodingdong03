package com.example.lenovo.baodingdong;

public class CunChuNeiRong {
    private String msg;
    private String shanchu;

    public CunChuNeiRong(String msg, String shanchu) {
        this.msg = msg;
        this.shanchu = shanchu;
    }

    public String getMsg() {
        return msg;
    }
    public String getShanchu() {
        return shanchu;
    }
}
